from __future__ import annotations

from typing import Optional

from openlineage.client.generated.base import RunFacet
from pydantic import BaseModel, Field
from typing_extensions import Annotated


class ErrorMessageRunFacet(RunFacet):
    message: Annotated[
        str,
        Field(
            example="org.apache.spark.sql.AnalysisException: Table or view not found: wrong_table_name; line 1 pos 14"
        ),
    ]
    """
    A human-readable string representing error message generated by observed system
    """
    programmingLanguage: Annotated[str, Field(example="JAVA")]
    """
    Programming language the observed system uses.
    """
    stackTrace: Annotated[
        Optional[str],
        Field(
            example='Exception in thread "main" java.lang.RuntimeException: A test exception\nat io.openlineage.SomeClass.method(SomeClass.java:13)\nat io.openlineage.SomeClass.anotherMethod(SomeClass.java:9)'
        ),
    ] = None
    """
    A language-specific stack trace generated by observed system
    """


class Model(BaseModel):
    errorMessage: Optional[ErrorMessageRunFacet] = None
